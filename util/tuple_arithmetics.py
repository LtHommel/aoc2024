def tup_add(a, b):
    """
    Performs '+' operation on two tuples of length two. For example:
    tup_add((4,2), (3,1)) returns (7,3)
    """
    return a[0] + b[0], a[1] + b[1]


def tup_sub(a, b):
    """
        Performs '-' operation on two tuples of length two. For example:
        tup_add((4,2), (3,1)) returns (1,1)
        """
    return a[0] - b[0], a[1] - b[1]